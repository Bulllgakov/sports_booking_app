// Функция для поиска городов по запросу
export function searchCities(query: string, limit: number = 5): typeof RUSSIAN_CITIES {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  
  // Сначала ищем точные совпадения с начала
  const exactMatches = RUSSIAN_CITIES.filter(city => 
    city.name.toLowerCase().startsWith(lowerQuery)
  );
  
  // Затем ищем частичные совпадения
  const partialMatches = RUSSIAN_CITIES.filter(city => 
    city.name.toLowerCase().includes(lowerQuery) && 
    !city.name.toLowerCase().startsWith(lowerQuery)
  );
  
  return [...exactMatches, ...partialMatches].slice(0, limit);
}